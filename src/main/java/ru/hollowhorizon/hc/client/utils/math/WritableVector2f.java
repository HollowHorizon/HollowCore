package ru.hollowhorizon.hc.client.utils.math;

public interface WritableVector2f {

    /**
     * Set the X value
     * @param x
     */
    void setX(float x);

    /**
     * Set the Y value
     * @param y
     */
    void setY(float y);

    /**
     * Set the X,Y values
     * @param x
     * @param y
     */
    void set(float x, float y);

}
