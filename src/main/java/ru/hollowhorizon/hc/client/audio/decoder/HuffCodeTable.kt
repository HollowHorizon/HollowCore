        @JvmStatic
        fun huffmanDecoder(h: HuffCodeTable, x: IntArray, y: IntArray, v: IntArray, w: IntArray, br: BitReserve): Int {
            val dmask = 1 shl 4 * 8 - 1
            var level: Int
            var point = 0
            var error = 1
            level = dmask

            if (h.value == null) return 2

            if (h.treelen == 0) {
                y[0] = 0
                x[0] = 0
                return 0
            }

            do {
                if (h.value!![point][0] == 0) {
                    x[0] = h.value!![point][1] ushr 4
                    y[0] = h.value!![point][1] and 0xf
                    error = 0
                    break
                }

                if (br.hget1bit() != 0) {
                    while (h.value!![point][1] >= MXOFF) point += h.value!![point][1]
                    point += h.value!![point][1]
                } else {
                    while (h.value!![point][0] >= MXOFF) point += h.value!![point][0]
                    point += h.value!![point][0]
                }
                level = level ushr 1
            } while (level != 0 || point < 0)

            if (h.tablename0 == '3' && (h.tablename1 == '2' || h.tablename1 == '3')) {
                v[0] = y[0] shr 3 and 1
                w[0] = y[0] shr 2 and 1
                x[0] = y[0] shr 1 and 1
                y[0] = y[0] and 1

                if (v[0] != 0 && br.hget1bit() != 0) v[0] = -v[0]
                if (w[0] != 0 && br.hget1bit() != 0) w[0] = -w[0]
                if (x[0] != 0 && br.hget1bit() != 0) x[0] = -x[0]
                if (y[0] != 0 && br.hget1bit() != 0) y[0] = -y[0]
            } else {
                if (h.linbits != 0 && h.xlen - 1 == x[0]) x[0] += br.hgetbits(h.linbits)
                if (x[0] != 0 && br.hget1bit() != 0) x[0] = -x[0]
                if (h.linbits != 0 && h.ylen - 1 == y[0]) y[0] += br.hgetbits(h.linbits)
                if (y[0] != 0 && br.hget1bit() != 0) y[0] = -y[0]
            }
            return error
        }

